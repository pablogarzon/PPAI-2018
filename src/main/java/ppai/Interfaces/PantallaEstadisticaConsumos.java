    private void cargarComboCategorias(){
        this.jComboBox1.addItem("Todas");
        for (String cat : gestor.buscarCategorias()) {
            this.jComboBox1.addItem(cat);
        }
    }
    
    private void cargarComboLocalidades(){
        this.jComboBox3.addItem("Todas");
        for (String localidad : gestor.buscarLocalidades()) {
            this.jComboBox3.addItem(localidad);
        }
    }
    
    private void cargarListaZonas(String filtro){
        this.list1.removeAll();
        for (String localidad : gestor.buscarZonas(filtro)) {
            this.list1.add(localidad);
        }
    }
    
    private void cargarComboMetodoEstadistico(){
        this.jComboBox2.addItem("Media Con Desviacion Estandard");
        this.jComboBox2.addItem("Promedio Normalizado");
        this.jComboBox2.addItem("Sumatoria");
    }

