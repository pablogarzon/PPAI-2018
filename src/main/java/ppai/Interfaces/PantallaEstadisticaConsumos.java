    private void jDateChooser2FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jDateChooser2FocusLost
        // TODO add your handling code here:
        Date fechaDesde = this.jDateChooser1.getDate();
        Date fechaHasta = this.jDateChooser2.getDate();
        if ((fechaDesde != null && fechaHasta != null)
                && fechaDesde.before(fechaHasta)
                && (fechaHasta.before(new Date()) || fechaHasta.equals(new Date()))) {
            hayError = false;
        } else {
            hayError = true;
        }

        if (hayError) {
            this.jLabel7.setText("Rango de fechas invalidos");
            this.jLabel7.setVisible(hayError);
        }
    }//GEN-LAST:event_jDateChooser2FocusLost

